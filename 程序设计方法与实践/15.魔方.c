int cube[54];
int op[][2] = {{'U',1},{'D',2},{'L',3},{'R',4},{'F',5},{'B',6},{'X',7},{'Y',8},{'Z',9}};
void deal(int p)
{
	int tmp[54], i;
	for(i=0;i<54;i++)
		tmp[i] = cube[map[p][i]];
	memcpy(cube, tmp, sizeof(cube));
}

int covert(char ch)
{
	int i, t=(islower(ch)?3:1);
	ch = toupper(ch);
	for(i=0;i<9;i++)
		if(op[i][0]==ch)
		{
			while(t--)
				deal(op[i][1]);
			return 1;
		}
	return 0;
}
int main()
{
	char ch = 0;
	int i, flag = 1;

	for(i=0;i<54;i++)
		cube[i] = i;
	while(1)
	{
		ch = getchar();
		if(ch==-1 || ch=='\n' || !covert(ch))
			break;
	}
	for(i=0;i<54;i++)
	{
		if(cube[i] != i)
			flag = 0;
	}
	printf(flag?"Yes\n":"No\n");
	return 0;
}